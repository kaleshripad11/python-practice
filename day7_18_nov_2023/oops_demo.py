# Python also support OOPS concepts

# Defining class with two methods in it class_function1() & class_function2()
class PythonClass:
    def class_function1(self):
        # The keyword represents class name
        pass

    def class_function2(self):
        print("Hey there, this is method from class")


# Creating object of class
pc = PythonClass()

# Calling class methods using object
pc.class_function1()
pc.class_function2()

