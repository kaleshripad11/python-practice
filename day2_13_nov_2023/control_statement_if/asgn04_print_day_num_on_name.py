# Print day number on provided day name
day = input("Please enter your day[Ex. Sun, Mon, Tue, Wed, Thu, Fri, Sat] : ")
if day == 'Sun':
    print("1")
elif day == 'Mon':
    print("2")
elif day == 'Tue':
    print("3")
elif day == 'Wed':
    print("4")
elif day == 'Thu':
    print("5")
elif day == 'Fri':
    print("6")
elif day == 'Sat':
    print("7")
else:
    print("Please enter a valid day as mentioned in the first line & and try again!!!")
