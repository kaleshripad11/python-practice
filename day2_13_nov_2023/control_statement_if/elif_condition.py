# Elif is again extension of if-else condition
# In this statement multiple if statements can be written

day = int(input("Please enter your favourite no between 1 to 7 :"))

if day == 1:
    print("Monday")
elif day == 2:
    print("Tuesday")
elif day == 3:
    print("Wednesday")
elif day == 4:
    print("Thursday")
elif day == 5:
    print("Friday")
elif day == 6:
    print("Saturday")
elif day == 7:
    print("Sunday")
else:
    print("You entered invalid number")

